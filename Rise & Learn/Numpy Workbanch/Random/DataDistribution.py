# The data distribution can be visualized using histograms, box plots, or density plots to understand the shape and spread of the data.
import numpy as np

# Random integers
data = np.random.randint(1, 100, size=10)
print("Random Data:", data)

# Mean & Std - Measures of centrol tendency and dispersion
print("Mean:", np.mean(data))
print("Std Dev:", np.std(data))

